    // Hàm taok SKU
    private function makeSku($id, $type) {
        $sku = 'VH57';
        $type = trim($type);
        // Loại bỏ dấu từ các ký tự tiếng Việt
        $type = $this->removeAccents($type);
        $words = explode(' ', $type);
        foreach ($words as $word) {
            if (!empty($word)) {
                $sku .= strtoupper($word[0]);
            }
        }
        // Đảm bảo rằng id luôn có 4 chữ số bằng cách thêm các số 0 ở đầu
        $sku .= str_pad($id, 4, '0', STR_PAD_LEFT);
        return $sku;
    }

    public function setSku() {
        $this->sku = $this->makeSku($this->id, $this->type);
        $this->save();
    }

    // Hàm tạo sale 
    private function makeSale($original, $discount) {
        if ($discount === null || $original == 0) {
            return "none";
        } else {
            $percent = round((1 - $discount / $original) * 100);
            return $percent . "%";
        }
    }

    public function setSale() {
        $this->sale = $this->makeSale($this->original_price, $this->discount_price);
        $this->save();
    }
    




